"""
Catálogo de perfiles laminados estándar.

Datos tomados de catálogos comerciales (ArcelorMittal / EN 10025) y de
Eurocódigo. Unidades:
    A   en mm²
    Iy  en mm⁴ (eje fuerte, paralelo a las alas)
    Iz  en mm⁴ (eje débil, perpendicular a las alas)
    h, b en mm (canto y anchura del perfil)

El catálogo expone cuatro familias y un diccionario unificado `PERFILES`.
"""

# Helper para convertir las unidades habituales del catálogo
# (A en cm², I en cm⁴) a SI (mm²/mm⁴).
def _entry(tipo, h, b, A_cm2, Iy_cm4, Iz_cm4):
    return {
        "tipo": tipo,
        "h":  h,
        "b":  b,
        "A":  A_cm2 * 100.0,        # cm² → mm²
        "Iy": Iy_cm4 * 1.0e4,       # cm⁴ → mm⁴
        "Iz": Iz_cm4 * 1.0e4,
    }

def _ipe(h, b, A, Iy, Iz):  return _entry("IPE", h, b, A, Iy, Iz)
def _heb(h, b, A, Iy, Iz):  return _entry("HEB", h, b, A, Iy, Iz)
def _hea(h, b, A, Iy, Iz):  return _entry("HEA", h, b, A, Iy, Iz)
def _upn(h, b, A, Iy, Iz):  return _entry("UPN", h, b, A, Iy, Iz)


